"""Faça um algoritmo que receba três notas, calcule e mostre a média 
aritmética entre elas."""

""""Receber notas de um aluno e calcular a média aritmética"""

Nota = float(input("Digite aqui a Primerira nota: "))
Nota2 = float(input("Digite aqui a Primerira nota: "))
Nota3 = float(input("Digite aqui a Primerira nota: "))
media = (Nota + Nota2 + Nota3) / 3
print(f"A sua media é de: {media:.2f}")